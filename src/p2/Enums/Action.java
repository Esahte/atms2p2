package p2.Enums;

/**
 * The Action enum represents the various actions that can be performed.
 * It is used throughout the application to manage and track the actions.
 */
public enum Action {
    /**
     * The Close action indicates that a certain operation or task is being closed.
     */
    Close,

    /**
     * The Finish action indicates that a certain operation or task has been completed.
     */
    Finish,

    /**
     * The Open action indicates that a certain operation or task is being opened or initiated.
     */
    Open,

    /**
     * The Start action indicates that a certain operation or task has started.
     */
    Start
}