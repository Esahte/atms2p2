package p2.Enums;

/**
 * The TrainStatus enum represents the various states a train can be in.
 * It is used throughout the application to manage and track the status of trains.
 */
public enum TrainStatus {
    /**
     * The Completed status indicates that a train has finished its journey.
     */
    Completed,

    /**
     * The Initialised status indicates that a train has been set up and is ready to start its journey.
     */
    Initialised,

    /**
     * The Started status indicates that a train has begun its journey.
     */
    Started
}
